package WeatherMod;

public class WeatherCell {
    public float humidity;
    public float cloudiness;
    public float precipitation;
    public float windX;
    public float windY;

    public WeatherCell() {
        this.humidity = 0;
        this.cloudiness = 0;
        this.precipitation = 0;
        this.windX = 0;
        this.windY = 0;
    }
}
