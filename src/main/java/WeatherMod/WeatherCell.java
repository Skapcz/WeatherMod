package WeatherMod;

public class WeatherCell {
    public double humidity;
    public double cloudiness;
    public double precipitation;
    public double windX;
    public double windY;

    public WeatherCell() {
        this.humidity = 0;
        this.cloudiness = 0;
        this.precipitation = 0;
        this.windX = 0;
        this.windY = 0;
    }
}
